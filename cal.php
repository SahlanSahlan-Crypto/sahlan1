<?php
function sum($a, $b) {
	return $a + $b;
}
function multi($a, $b) {
	return $a * $b;
}
function div($a, $b) {
	return $a / $b;
}
function sub($a, $b) {
	return $a - $b;
}
echo "Sum is:". sum(4, 5);
echo"<br>";
